"use strict";

// 2d object
class Object2D {
    constructor() {
        this.width;
        this.height;
        this.x;
        this.y;
    }
    setWidth(width) {
        this.width = width;
    }
    setHeight(height) {
        this.height = height;
    }
    setX(x) {
        this.x = x;
    }
    setY(y) {
        this.y = y;
    }
}